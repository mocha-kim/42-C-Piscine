#include <stdlib.h>

int		ft_ultimate_range(int **range, int min, int max)
{
	int i;
	int	*arr;

	if (min >= max)
		return (0);
	if (!(arr = (int *)malloc((max - min) * sizeof(int))))
		return (-1);
	i = 0;
	while (min < max)
	{
		arr[i] = min;
		min++;
		i++;
	}
	*range = arr;
	return (i);
}
